#include<bits/stdc++.h>

int main()
{
    std::string S;
    std::cin>>S;

    if(S=="A")
    {
        std::cout<<"T"<<std::endl;
    }
    else if(S=="T")
    {
        std::cout<<"A"<<std::endl;
    }
    else if(S=="G")
    {
        std::cout<<"C"<<std::endl;
    }
    else
    {
        std::cout<<"G"<<std::endl;
    }
}